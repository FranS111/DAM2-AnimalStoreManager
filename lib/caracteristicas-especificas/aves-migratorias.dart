// Definición del mixin AvesMigratoriasMixin
// Este mixin agrega atributos relacionados con la migración de aves migratorias
mixin AvesMigratoriasMixin {
  late String tipoMigracion;      // Tipo de migración (por ejemplo, "estacional")
  late String destinoMigracion;  // Destino de la migración (por ejemplo, "sur")
  late int distanciaMigracion;   // Distancia de la migración en kilómetros
}
